    def draw_addon_structured(desc_raw, cost_str):
        """Parse addon description and render as structured bullets."""
        global y
        cv.setFillColor(GRAY); cv.setFont('Helvetica', 8.5)
        cv.drawString(ML + 8, y, '1')
        cost_y = y
        dx = ML + qW + 15
