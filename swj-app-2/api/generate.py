        # Parse: "Primary bedroom (Queen) — Medium: scope..." or "Room: scope..." or custom
        # Header shown to client = room name only (strip bed size and room size)
        # Scope = everything after the first ': '
        import re
        if ': ' in desc_raw:
            raw_header, scope_part = desc_raw.split(': ', 1)
        else:
            raw_header = desc_raw
            scope_part = ''
    
        # Strip bed size "(Queen)" and size tier "— Medium" from header
        # Keep only the room name e.g. "Primary bedroom" or custom label
        clean_header = re.sub(r'\s*\([^)]+\)', '', raw_header)   # remove (Queen), (King) etc
        clean_header = re.sub(r'\s*—.*$', '', clean_header)         # remove — Medium, — Small etc
        clean_header = clean_header.strip()
    
        # Bold header — room name only
        cv.setFont('Helvetica-Bold', 8.5); cv.setFillColor(BLACK)
        for l in wrap(cv, clean_header, 'Helvetica-Bold', 8.5, descW):
            cv.drawString(dx, ry, l); ry -= 11
    
        # Render scope as structured bullets if it contains comma-separated items
        # or just as a readable paragraph broken into bullets
        if scope_part:
            ry -= 3
            # Split scope into bullet points by sentence or comma
            import re
            # Split on '. ' or ', ' to create bullets
            raw_items = re.split(r',\s*(?=[A-Z])|(?<=\.)\s+(?=[A-Z])', scope_part)
            if len(raw_items) > 1:
                cv.setFont('Helvetica', 8.5); cv.setFillColor(BLACK)
                for item in raw_items:
                    item = item.strip().rstrip('.')
